#!/usr/bin/env python3
"""Render report.md pivots from outcomes.csv.

No verdict text is hard-coded with conclusions; thresholds applied verbatim
from xenon#596 conventions (see ../README.md).
"""
from __future__ import annotations

import csv
from collections import defaultdict
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
OUTCOMES = ROOT / "outcomes.csv"
REPORT = ROOT / "report.md"

EXPAND_FLOOR = 3
LATE_REVIEW_HIGH = 0.30
SECURITY_FOLLOWUP_HIGH = 0.20
LATE_REVIEW_OK = 0.20
SECURITY_FOLLOWUP_OK = 0.10


def verdict(driver: str, n: int, late_rate: float, sec_rate: float) -> str:
    if n < EXPAND_FLOOR:
        return "INCONCLUSIVE"
    cheap = driver in {"codex", "gemini"}
    if cheap and late_rate >= LATE_REVIEW_HIGH:
        return "PAUSE_CHEAP"
    if not cheap and (
        late_rate >= LATE_REVIEW_HIGH or sec_rate >= SECURITY_FOLLOWUP_HIGH
    ):
        return "WATCH_STRONG"
    if not cheap and (
        late_rate <= LATE_REVIEW_OK and sec_rate <= SECURITY_FOLLOWUP_OK
    ):
        return "STRONG_OK"
    if cheap and late_rate <= 0.10:
        return "EXPAND_CHEAP"
    return "REVIEW_MEDIUM"


def main() -> None:
    rows: list[dict] = []
    with OUTCOMES.open() as f:
        rows = list(csv.DictReader(f))

    by_driver: dict[str, list[dict]] = defaultdict(list)
    by_risk: dict[str, list[dict]] = defaultdict(list)
    by_bucket: dict[tuple[str, str], list[dict]] = defaultdict(list)
    for r in rows:
        by_driver[r["driver_preference"]].append(r)
        by_risk[r["risk_class"]].append(r)
        by_bucket[(r["risk_class"], r["driver_preference"])].append(r)

    def metric(rs: list[dict]) -> dict:
        n = len(rs)
        merged = sum(1 for r in rs if r["outcome_class"].startswith("merged"))
        late_pointer = sum(
            1 for r in rs if r["outcome_class"] == "merged_late_pointer"
        )
        open_stale = sum(1 for r in rs if r["outcome_class"] == "open_stale")
        closed = sum(1 for r in rs if r["outcome_class"] == "closed_unmerged")
        return {
            "n": n,
            "merged": merged,
            "late_pointer": late_pointer,
            "open_stale": open_stale,
            "closed_unmerged": closed,
            "late_rate": (open_stale + late_pointer) / n if n else 0.0,
            # security_followup is a follow-up bead count from bd; this artifact
            # does not scrape that. Emit 0.0 so the verdict gate cannot fire on
            # a tautological "share of production_security in the bucket".
            "security_followup_rate": 0.0,
        }

    out: list[str] = []
    out.append("# xc-m7mq routing-accuracy eval — report")
    out.append("")
    out.append(
        "Generated by `scripts/render_report.py` from `outcomes.csv`. "
        "See [README.md](README.md) for taxonomy, scope, and thresholds."
    )
    out.append("")
    out.append(f"**Total PRs in scope:** {len(rows)}")
    out.append("")
    out.append("## Outcome class counts")
    out.append("")
    counts: dict[str, int] = defaultdict(int)
    for r in rows:
        counts[r["outcome_class"]] += 1
    out.append("| outcome_class | n |")
    out.append("|---|---:|")
    for k in sorted(counts):
        out.append(f"| {k} | {counts[k]} |")
    out.append("")

    out.append("## By driver_preference")
    out.append("")
    out.append(
        "| driver | n | merged | late_pointer | open_stale | closed_unmerged | "
        "late_rate |"
    )
    out.append("|---|---:|---:|---:|---:|---:|---:|")
    for d in sorted(by_driver):
        m = metric(by_driver[d])
        out.append(
            f"| {d} | {m['n']} | {m['merged']} | {m['late_pointer']} | "
            f"{m['open_stale']} | {m['closed_unmerged']} | "
            f"{m['late_rate']:.2f} |"
        )
    out.append("")

    out.append("## By risk_class")
    out.append("")
    out.append(
        "| risk_class | n | merged | late_pointer | open_stale | "
        "closed_unmerged | late_rate |"
    )
    out.append("|---|---:|---:|---:|---:|---:|---:|")
    for k in sorted(by_risk):
        m = metric(by_risk[k])
        out.append(
            f"| {k} | {m['n']} | {m['merged']} | {m['late_pointer']} | "
            f"{m['open_stale']} | {m['closed_unmerged']} | {m['late_rate']:.2f} |"
        )
    out.append("")

    out.append("## Bucket verdicts (risk_class × driver_preference)")
    out.append("")
    out.append(
        "Verdicts use the xenon#596 thresholds verbatim. `late_rate` here is "
        "the proxy `(open_stale + merged_late_pointer) / n` — an upper bound "
        "without per-PR review-comment scrape. Buckets with `n < 3` are "
        "INCONCLUSIVE by definition."
    )
    out.append("")
    out.append(
        "| risk_class | driver | n | late_rate | verdict |"
    )
    out.append("|---|---|---:|---:|---|")
    for key in sorted(by_bucket):
        risk, driver = key
        rs = by_bucket[key]
        m = metric(rs)
        v = verdict(
            driver, m["n"], m["late_rate"], m["security_followup_rate"]
        )
        out.append(
            f"| {risk} | {driver} | {m['n']} | {m['late_rate']:.2f} | {v} |"
        )
    out.append("")

    out.append("## Notes / caveats")
    out.append("")
    out.append(
        "- `late_rate` here is a **proxy**: open_stale (>48h since "
        "creation) + merged_late_pointer (pointer/bump/refresh PRs that took "
        ">24h to merge). It does not include reviewer round-trips on body "
        "PRs that nonetheless merged inside the window. True `late_review` "
        "would require per-PR review-comment timestamps; that scrape is out "
        "of scope here (see `xc-tx49i` for the bounded swarm-improver "
        "evidence loop)."
    )
    out.append(
        "- `security_followup_rate` is **not measured** by this artifact. "
        "The xenon#596 baseline read it from a per-PR follow-up bead count "
        "in the bd store; that scrape is out of scope here. We pin it to "
        "0.0 in the verdict gate so verdicts fire only on `late_rate`."
    )
    out.append(
        "- `risk_class` is keyword-inferred. `unclassified` rows are real "
        "evidence — they signal the taxonomy needs another rule or a label "
        "scrape. They are not silently bucketed."
    )
    out.append(
        "- `driver_preference` is head-ref-inferred. `xsm-internal` covers "
        "PRs landed by the xsm/landing lanes themselves (often pointer "
        "bumps or refinement-fixture digest tests); they are listed but "
        "should not be read as a driver-quality signal."
    )
    out.append(
        "- The window includes **two distinct merge spikes**: 2026-04-26 "
        "(refinement fixture digests, xsm-internal) and 2026-04-28..29 "
        "(swarm-reliability fixes after the supervisor-as-landing-lane "
        "transition on 2026-04-19). This concentrates `xsm_control_plane`."
    )
    out.append("")

    out.append("## Recommended follow-ups (no edits applied here)")
    out.append("")
    out.append(
        "Per xc-m7mq AC, this eval does not change `driver_preference`, "
        "dispatcher policy, or `xsm.routing_accuracy` thresholds. Below are "
        "the candidate strategy-tune beads to file *iff* the evidence above "
        "holds after a real `late_review` scrape:"
    )
    out.append("")
    out.append(
        "1. Buckets with `EXPAND_CHEAP` verdict + n ≥ 3: candidates for a "
        "`driver_preference: cheap` widening bead. **Do not** widen on "
        "proxy-only late_rate; require a follow-up bead that re-runs the "
        "eval after a per-PR review-comment scrape (xc-tx49i)."
    )
    out.append(
        "2. Buckets with `WATCH_STRONG` verdict: candidates for the "
        "`xc-yzkw` strong-review-receipt gate (already on backlog)."
    )
    out.append(
        "3. `unclassified` ≥ 5: candidates for an explicit-label bead so "
        "future evals do not need keyword inference."
    )
    out.append("")

    REPORT.write_text("\n".join(out) + "\n")
    print(f"wrote {REPORT}")


if __name__ == "__main__":
    main()
